const Photo = require('../models/Photo');
const Album = require('../models/Album');
const LoveTree = require('../models/LoveTree');
const TimelineEvent = require('../models/TimelineEvent');
const awardXP = require('../utils/awardXP');

const requireRelationship = (req, res) => {
  if (!req.user.relationshipId) {
    res.status(400).json({ success: false, message: 'Not in a relationship' });
    return false;
  }
  return true;
};

exports.uploadPhoto = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  if (!req.file) return res.status(400).json({ success: false, message: 'Photo file required' });

  const { caption, albumId, locationName, lat, lng } = req.body;
  const photo = await Photo.create({
    relationshipId: req.user.relationshipId,
    uploaderId: req.user._id,
    url: req.file.cloudUrl,
    caption: caption || '',
    albumId: albumId || null,
    location: { name: locationName || '', lat: lat ? Number(lat) : undefined, lng: lng ? Number(lng) : undefined },
  });

  const photoCount = await Photo.countDocuments({ relationshipId: req.user.relationshipId, isDeleted: false });
  if (photoCount === 1) {
    await TimelineEvent.create({
      relationshipId: req.user.relationshipId,
      eventType: 'first_photo',
      title: 'First Memory 📸',
      description: 'You created your first memory!',
      eventDate: new Date(),
      isAutoGenerated: true,
    });
  }

  const tree = await LoveTree.findOne({ relationshipId: req.user.relationshipId });
  if (tree) { tree.photoPoints += 3; tree.points += 3; tree.lastWatered = new Date(); await tree.save(); }

  awardXP(req.user.relationshipId, 10); // +10 XP per photo
  res.status(201).json({ success: true, message: 'Photo uploaded', data: { photo } });
};

exports.getPhotos = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { albumId, page = 1, limit = 20, from, to } = req.query;
  const query = { relationshipId: req.user.relationshipId, isDeleted: false };
  if (albumId) query.albumId = albumId;
  if (from || to) {
    query.createdAt = {};
    if (from) query.createdAt.$gte = new Date(from);
    if (to) query.createdAt.$lte = new Date(to);
  }
  const photos = await Photo.find(query)
    .populate('uploaderId', 'name nickname')
    .sort({ createdAt: -1 })
    .skip((page - 1) * limit)
    .limit(Number(limit));
  const total = await Photo.countDocuments(query);
  res.json({ success: true, data: { photos, total, page: Number(page) } });
};

exports.deletePhoto = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const photo = await Photo.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!photo) return res.status(404).json({ success: false, message: 'Photo not found' });
  photo.isDeleted = true;
  await photo.save();
  res.json({ success: true, message: 'Photo deleted' });
};

