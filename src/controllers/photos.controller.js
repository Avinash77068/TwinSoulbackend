exports.addComment = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { text } = req.body;
  if (!text) return res.status(400).json({ success: false, message: 'Comment text required' });
  const photo = await Photo.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!photo) return res.status(404).json({ success: false, message: 'Photo not found' });
  photo.comments.push({ userId: req.user._id, text });
  await photo.save();
  await photo.populate('comments.userId', 'name nickname');
  res.json({ success: true, message: 'Comment added', data: { comments: photo.comments } });
};

exports.deleteComment = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const photo = await Photo.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!photo) return res.status(404).json({ success: false, message: 'Photo not found' });
  const comment = photo.comments.id(req.params.commentId);
  if (!comment) return res.status(404).json({ success: false, message: 'Comment not found' });
  if (comment.userId.toString() !== req.user._id.toString()) {
    return res.status(403).json({ success: false, message: 'Cannot delete this comment' });
  }
  comment.deleteOne();
  await photo.save();
  res.json({ success: true, message: 'Comment deleted' });
};

