exports.searchPhotos = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { q } = req.query;
  if (!q) return res.status(400).json({ success: false, message: 'Search query required' });
  const photos = await Photo.find({
    relationshipId: req.user.relationshipId,
    isDeleted: false,
    $or: [
      { caption: { $regex: q, $options: 'i' } },
      { 'location.name': { $regex: q, $options: 'i' } },
    ],
  }).sort({ createdAt: -1 }).limit(50);
  res.json({ success: true, data: { photos } });
};

exports.getAlbums = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const albums = await Album.find({ relationshipId: req.user.relationshipId, isDeleted: false })
    .populate('creatorId', 'name nickname').sort({ createdAt: -1 });
  const albumsWithCount = await Promise.all(albums.map(async (album) => {
    const count = await Photo.countDocuments({ albumId: album._id, isDeleted: false });
    return { ...album.toObject(), photoCount: count };
  }));
  res.json({ success: true, data: { albums: albumsWithCount } });
};

