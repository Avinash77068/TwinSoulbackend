exports.forwardMessage = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const original = await Message.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!original) return res.status(404).json({ success: false, message: 'Message not found' });

  const forwarded = await Message.create({
    relationshipId: req.user.relationshipId,
    senderId: req.user._id,
    content: `↩ ${original.content}`,
    type: original.type,
    mediaUrl: original.mediaUrl,
  });
  res.status(201).json({ success: true, message: 'Message forwarded', data: { message: forwarded } });
};
