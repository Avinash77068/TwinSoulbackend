const Message = require('../models/Message');
const User = require('../models/User');
const LoveTree = require('../models/LoveTree');
const TimelineEvent = require('../models/TimelineEvent');
const awardXP = require('../utils/awardXP');
const { getIo } = require('../config/socketInstance');

const requireRelationship = (req, res) => {
  if (!req.user.relationshipId) {
    res.status(400).json({ success: false, message: 'Not in a relationship' });
    return false;
  }
  return true;
};

const CHAT_STAGES = [
  { name: 'seed', min: 0 }, { name: 'plant', min: 50 }, { name: 'tree', min: 200 },
  { name: 'blooming', min: 500 }, { name: 'golden', min: 1000 }, { name: 'legendary', min: 2000 },
];

const addLoveTreePoints = async (relationshipId, points, field) => {
  const tree = await LoveTree.findOne({ relationshipId });
  if (!tree) return;
  // Heal legacy documents that may have undefined/NaN numeric fields
  tree[field] = (Number(tree[field]) || 0) + points;
  tree.points = (Number(tree.points) || 0) + points;
  // Bug fix: always fall back to CHAT_STAGES[0] so stage.name never throws
  const stage = CHAT_STAGES.filter(s => tree.points >= s.min).pop() || CHAT_STAGES[0];
  tree.stage = stage.name;
  tree.lastWatered = new Date();
  await tree.save();
};

exports.getMessages = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { page = 1, limit = 50, before } = req.query;
  console.log(`[Chat] getMessages | user: ${req.user._id} | page: ${page} | limit: ${limit} | before: ${before || 'N/A'}`);

  const query = { relationshipId: req.user.relationshipId, isDeleted: false };
  if (before) query.createdAt = { $lt: new Date(before) };

  const messages = await Message.find(query)
    .populate('senderId', 'name nickname profilePhoto')
    .populate('replyTo')
    .sort({ createdAt: -1 })
    .skip((page - 1) * limit)
    .limit(Number(limit));

  const total = await Message.countDocuments(query);
  console.log(`[Chat] getMessages result | fetched: ${messages.length} | total: ${total}`);
  res.json({ success: true, data: { messages: messages.reverse(), total, page: Number(page) } });
};

exports.sendMessage = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { content, type = 'text', replyTo, isSecret = false } = req.body;
  console.log(`[Chat] sendMessage | user: ${req.user._id} | type: ${type} | isSecret: ${isSecret} | content: "${content?.slice(0, 50)}"`);

  if (!content && !req.file) {
    console.log(`[Chat] sendMessage failed | no content and no file`);
    return res.status(400).json({ success: false, message: 'Content or file required' });
  }

  const message = await Message.create({
    relationshipId: req.user.relationshipId,
    senderId: req.user._id,
    content: content || '',
    type,
    mediaUrl: req.file ? req.file.cloudUrl : '',
    replyTo: replyTo || null,
    isSecret,
  });
  console.log(`[Chat] Message saved to DB | messageId: ${message._id}`);

  await User.findByIdAndUpdate(req.user._id, {
    $push: {
      messages: {
        messageId: message._id,
        content: message.content,
        type: message.type,
        mediaUrl: message.mediaUrl,
        isSecret: message.isSecret,
        sentAt: message.createdAt,
        toRelationshipId: message.relationshipId,
      },
    },
  });
  console.log(`[Chat] Message pushed to user.messages array | userId: ${req.user._id}`);

  const msgCount = await Message.countDocuments({ relationshipId: req.user.relationshipId });
  console.log(`[Chat] Total messages in relationship: ${msgCount}`);
  if (msgCount === 1) {
    console.log(`[Chat] First message! Creating timeline event 'first_chat'`);
    await TimelineEvent.create({
      relationshipId: req.user.relationshipId,
      eventType: 'first_chat',
      title: 'First Message 💌',
      description: 'You sent your first message!',
      eventDate: new Date(),
      isAutoGenerated: true,
    });
    console.log(`[Chat] Timeline event 'first_chat' created`);
  }

  await addLoveTreePoints(req.user.relationshipId, 1, 'chatPoints');
  awardXP(req.user.relationshipId, 2); // +2 XP per message
  await message.populate('senderId', 'name nickname profilePhoto');

  const io = getIo();
  if (io) {
    const room = `relationship:${req.user.relationshipId}`;
    console.log(`[Chat] Emitting message:new to room: ${room} | messageId: ${message._id}`);
    io.to(room).emit('message:new', message);
  } else {
    console.warn(`[Chat] Socket IO not available, message:new not emitted`);
  }

  console.log(`[Chat] sendMessage success | messageId: ${message._id}`);
  res.status(201).json({ success: true, message: 'Message sent', data: { messageId: message._id, message } });
};

exports.deleteMessage = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const msg = await Message.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!msg) return res.status(404).json({ success: false, message: 'Message not found' });
  if (msg.senderId.toString() !== req.user._id.toString()) {
    return res.status(403).json({ success: false, message: 'Cannot delete partner message' });
  }
  msg.isDeleted = true;
  await msg.save();
  res.json({ success: true, message: 'Message deleted' });
};

exports.reactToMessage = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { emoji } = req.body;
  if (!emoji) return res.status(400).json({ success: false, message: 'Emoji required' });

  const msg = await Message.findOne({ _id: req.params.id, relationshipId: req.user.relationshipId });
  if (!msg) return res.status(404).json({ success: false, message: 'Message not found' });

  const existing = msg.reactions.find(r => r.userId.toString() === req.user._id.toString());
  if (existing) existing.emoji = emoji;
  else msg.reactions.push({ userId: req.user._id, emoji });

  await msg.save();
  res.json({ success: true, message: 'Reaction added', data: { reactions: msg.reactions } });
};

