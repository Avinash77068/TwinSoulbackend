exports.requestLeave = async (req, res) => {
  if (!req.user.relationshipId) {
    return res.status(400).json({ success: false, message: 'Not in a relationship' });
  }
  const relationship = await Relationship.findById(req.user.relationshipId);
  const uid = req.user._id.toString();

  if (relationship.user1.toString() === uid) relationship.user1WantsLeave = true;
  else relationship.user2WantsLeave = true;

  if (relationship.user1WantsLeave && relationship.user2WantsLeave) {
    relationship.status = 'ended';
    await relationship.save();
    await User.findByIdAndUpdate(relationship.user1, { isConnected: false, partnerId: null, relationshipId: null });
    await User.findByIdAndUpdate(relationship.user2, { isConnected: false, partnerId: null, relationshipId: null });
    return res.json({ success: true, message: 'Both confirmed. Connection ended.' });
  }

  await relationship.save();
  res.json({
    success: true,
    message: 'Leave request recorded. Waiting for partner confirmation.',
    data: { user1WantsLeave: relationship.user1WantsLeave, user2WantsLeave: relationship.user2WantsLeave },
  });
};

