const TimelineEvent = require('../models/TimelineEvent');

const requireRelationship = (req, res) => {
  if (!req.user.relationshipId) {
    res.status(400).json({ success: false, message: 'Not in a relationship' });
    return false;
  }
  return true;
};

exports.getTimeline = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { page = 1, limit = 30 } = req.query;
  const events = await TimelineEvent.find({
    relationshipId: req.user.relationshipId,
    isDeleted: false,
  }).sort({ eventDate: -1 }).skip((page - 1) * limit).limit(Number(limit));
  const total = await TimelineEvent.countDocuments({ relationshipId: req.user.relationshipId, isDeleted: false });
  res.json({ success: true, data: { events, total } });
};

exports.createEvent = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { title, description, eventDate, eventType = 'custom', mediaUrl } = req.body;
  if (!title || !eventDate) return res.status(400).json({ success: false, message: 'Title and date required' });

  const event = await TimelineEvent.create({
    relationshipId: req.user.relationshipId,
    eventType,
    title, description: description || '',
    mediaUrl: mediaUrl || (req.file ? `/uploads/${req.file.filename}` : ''),
    eventDate: new Date(eventDate),
    isAutoGenerated: false,
  });
  res.status(201).json({ success: true, message: 'Timeline event created', data: { event } });
};

exports.deleteEvent = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const event = await TimelineEvent.findOne({
    _id: req.params.id,
    relationshipId: req.user.relationshipId,
    isAutoGenerated: false,
  });
  if (!event) return res.status(404).json({ success: false, message: 'Event not found or auto-generated' });
  event.isDeleted = true;
  await event.save();
  res.json({ success: true, message: 'Timeline event deleted' });
};
