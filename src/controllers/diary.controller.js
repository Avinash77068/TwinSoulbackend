const Diary = require('../models/Diary');
const LoveTree = require('../models/LoveTree');
const TimelineEvent = require('../models/TimelineEvent');
const awardXP = require('../utils/awardXP');

const requireRelationship = (req, res) => {
  if (!req.user.relationshipId) {
    res.status(400).json({ success: false, message: 'Not in a relationship' });
    return false;
  }
  return true;
};

exports.createEntry = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { title, content, mood, isPrivate = false, tags } = req.body;
  if (!title || !content) return res.status(400).json({ success: false, message: 'Title and content required' });

  const entry = await Diary.create({
    relationshipId: req.user.relationshipId,
    authorId: req.user._id,
    title, content, mood: mood || 'happy',
    isPrivate,
    tags: tags || [],
    photos: req.uploadedUrls || [],
  });

  const entryCount = await Diary.countDocuments({ relationshipId: req.user.relationshipId });
  if (entryCount === 1) {
    await TimelineEvent.create({
      relationshipId: req.user.relationshipId,
      eventType: 'first_diary',
      title: 'First Diary Entry 📖',
      description: 'You wrote your first diary entry!',
      eventDate: new Date(),
      isAutoGenerated: true,
    });
  }

  const tree = await LoveTree.findOne({ relationshipId: req.user.relationshipId });
  if (tree) { tree.diaryPoints += 5; tree.points += 5; tree.lastWatered = new Date(); await tree.save(); }

  awardXP(req.user.relationshipId, 8); // +8 XP per diary entry
  res.status(201).json({ success: true, message: 'Diary entry created', data: { entry } });
};

exports.getEntries = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { page = 1, limit = 20 } = req.query;
  const query = {
    relationshipId: req.user.relationshipId,
    isDeleted: false,
    $or: [
      { isPrivate: false },
      { authorId: req.user._id },
    ],
  };
  const entries = await Diary.find(query)
    .populate('authorId', 'name nickname profilePhoto')
    .sort({ createdAt: -1 })
    .skip((page - 1) * limit)
    .limit(Number(limit));
  const total = await Diary.countDocuments(query);
  res.json({ success: true, data: { entries, total } });
};

exports.getEntry = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entry = await Diary.findOne({
    _id: req.params.id,
    relationshipId: req.user.relationshipId,
    isDeleted: false,
  }).populate('authorId', 'name nickname');

  if (!entry) return res.status(404).json({ success: false, message: 'Entry not found' });
  if (entry.isPrivate && entry.authorId._id.toString() !== req.user._id.toString()) {
    return res.status(403).json({ success: false, message: 'This entry is private' });
  }
  res.json({ success: true, data: { entry } });
};

exports.updateEntry = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entry = await Diary.findOne({
    _id: req.params.id,
    authorId: req.user._id,
    isDeleted: false,
  });
  if (!entry) return res.status(404).json({ success: false, message: 'Entry not found or not yours' });

  const { title, content, mood, isPrivate, tags } = req.body;
  if (title) entry.title = title;
  if (content) entry.content = content;
  if (mood) entry.mood = mood;
  if (isPrivate !== undefined) entry.isPrivate = isPrivate;
  if (tags) entry.tags = tags;

  if (req.body.removePhotos) {
    const toRemove = JSON.parse(req.body.removePhotos);
    entry.photos = (entry.photos || []).filter(p => !toRemove.includes(p));
  }
  if (req.uploadedUrls?.length) {
    entry.photos = [...(entry.photos || []), ...req.uploadedUrls];
  }

  await entry.save();
  res.json({ success: true, message: 'Entry updated', data: { entry } });
};

exports.deleteEntry = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entry = await Diary.findOne({ _id: req.params.id, authorId: req.user._id });
  if (!entry) return res.status(404).json({ success: false, message: 'Entry not found or not yours' });
  entry.isDeleted = true;
  await entry.save();
  res.json({ success: true, message: 'Entry deleted' });
};

exports.toggleFavorite = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entry = await Diary.findOne({
    _id: req.params.id, relationshipId: req.user.relationshipId, isDeleted: false,
  });
  if (!entry) return res.status(404).json({ success: false, message: 'Entry not found' });
  entry.isFavorite = !entry.isFavorite;
  await entry.save();
  res.json({ success: true, message: entry.isFavorite ? 'Added to favorites' : 'Removed from favorites' });
};

exports.getPrivateEntries = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entries = await Diary.find({
    authorId: req.user._id,
    isPrivate: true,
    isDeleted: false,
  }).sort({ createdAt: -1 });
  res.json({ success: true, data: { entries } });
};

exports.getSharedEntries = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const entries = await Diary.find({
    relationshipId: req.user.relationshipId,
    isPrivate: false,
    isDeleted: false,
  }).populate('authorId', 'name nickname').sort({ createdAt: -1 });
  res.json({ success: true, data: { entries } });
};

exports.searchEntries = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { q } = req.query;
  if (!q) return res.status(400).json({ success: false, message: 'Search query required' });
  const entries = await Diary.find({
    relationshipId: req.user.relationshipId,
    isDeleted: false,
    $or: [
      { title: { $regex: q, $options: 'i' } },
      { content: { $regex: q, $options: 'i' } },
    ],
    $and: [{ $or: [{ isPrivate: false }, { authorId: req.user._id }] }],
  }).populate('authorId', 'name nickname').sort({ createdAt: -1 }).limit(50);
  res.json({ success: true, data: { entries } });
};

exports.getCalendarView = async (req, res) => {
  if (!requireRelationship(req, res)) return;
  const { year, month } = req.query;
  const start = new Date(year || new Date().getFullYear(), (month || new Date().getMonth() + 1) - 1, 1);
  const end = new Date(start.getFullYear(), start.getMonth() + 1, 0, 23, 59, 59);

  const entries = await Diary.find({
    relationshipId: req.user.relationshipId,
    isDeleted: false,
    createdAt: { $gte: start, $lte: end },
    $or: [{ isPrivate: false }, { authorId: req.user._id }],
  }).select('title mood createdAt authorId isPrivate').populate('authorId', 'name');

  res.json({ success: true, data: { entries, year: start.getFullYear(), month: start.getMonth() + 1 } });
};
